public class Main {
		public static void main(String[] args) {
				System.out.println("Hello World!");
				// Два слеша блокируют коментарий в одну строку
				/* Слеш с звездочкой блокирует коментарий заключенный между ними
				 в независимости от количества написанных сток
				 */
				System.out.println("123456"); // Цифры как текст
				System.out.println(1234);// Цифры как цифры
				System.out.println("\tLine1\nLine2\n\tLine3\n\t\tLine4");
				System.out.println("10+50="+(10+50));
				System.out.println(10*20);
				System.out.println(10*20*10);
				System.out.println(10*10*10+20*20*20);
			}
	}
